int	ft_sqrt(int nb)
{
	int rac;

	rac = 1;
	if (nb < 0)
		return (0);
	while (rac <= nb / 2)
	{
		if (rac * rac == nb)
			return (rac);
		rac++;
	}
	return (0);
}
