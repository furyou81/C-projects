int	div(int a, int b)
{
	return (a / b);
}

int	mod(int a, int b)
{
	return (a % b);
}

int	plus(int a, int b)
{
	return (a + b);
}

int	moins(int a, int b)
{
	return (a - b);
}

int	mult(int a, int b)
{
	return (a * b);
}
